  <h1 class="headline-right" style="text-align: center;">the result</h1>
  <p class="summary">
    The design principles of the app focus on four core values. Reliability is
    emphasized by the selective use of yellow, the brand's energetic color, to
    highlight crucial elements without creating an overly playful look.
    Innovation and intrigue are achieved through a frameless, seamless
    interaction design, with services readily accessible via drag-and-drop
    without page transitions or loading times. To ensure customers feel in
    control of their finances, the app prominently displays personal assets,
    income, outcomes, and recent transactions on the homepage, with
    user-friendly infographics for asset management. Lastly, empathy and
    companionship are prioritized by providing easy access to customer service
    through an AI chatbot or help desk, ensuring a supportive user experience
    throughout the app.
  </p>
  <p class="summary">
    Overall, my experience at NEXTBANK was very rewarding. It was my first time
    leading a design team and doing a design sprint, and I also had to act as a
    'kind-of' product manager. I wrote all the specifications, communicated with
    the engineers, controlled the schedule, and did stakeholder management. I
    learned a lot from this project and am grateful for the opportunity to be
    involved in product development for a cool brand.
  </p>
