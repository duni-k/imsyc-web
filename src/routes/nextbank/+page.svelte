    design, and developing the design system in collaboration with UI designers,
    visual designers, and the corporate identity team. Our goal was to provide a
    fresh in-app experience that conveyed NEXTBANK's innovative and futuristic
    vision.
  </p>
  <p class="summary">
    To achieve this, the design team worked closely together to create an
    innovative and user-friendly design that would set NEXTBANK apart from other
    financial services. We aimed to provide an exceptional in-app experience
    that was both intuitive and engaging. With the help of a strong design
    system, we were able to maintain consistency across different screens and
    improve the overall user experience.
  </p>

